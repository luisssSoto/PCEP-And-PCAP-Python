'''What is Python?
Python is a high-level, interpreted, object-oriented, and/or 
procedural language. The author of this language is Guido Van 
Rossum, who named it after his fondness for the BBC series 
'Monty Python's Flying Circus,' not for his liking for these 
charming and respected reptiles.

Features:
*Easy to learn as a new student
*Easy to teach as it does not have rules as exclusive as other languages
*Easy to use because it uses fewer lines of code than its rivals
*Easy to understand another programmer's code
*Easy to obtain as it is free and open-source'''