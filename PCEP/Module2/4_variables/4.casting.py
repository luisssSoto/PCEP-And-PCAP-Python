print("There is an importan knowledge about change any value for another one, that concept is called \"casting\":")
floatBox = -6065.58E3
print(floatBox)
print(type(floatBox),end='\n')

intBox = int(floatBox)
print(intBox)
print(type(intBox),end='\n')

stringBox = str(intBox)
print(stringBox)
print(type(stringBox),end='\n')

booleanBox = bool(stringBox)
print(booleanBox)
print(type(booleanBox),end='\n')