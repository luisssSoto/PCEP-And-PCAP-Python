'''Strange Operations'''

print('Division')
print('first level:')
print((8/4), (-8/4), (8/-4), (-8/-4), '\n')

print('second level:')
print((4/8), (-4/8), (4/-8), (-4/-8), '\n')

print('Floor Division')
print(('first level:'))
print((8//6), (-8//6), (8//-6), (-8//-6), '\n')

print('second level:')
print((6//8), (-6//8), (6//-8), (-6//-8), '\n')

print('Module')
print('first level:')
print((6%4), (-6%4), (6%-4), (-6%-4),'\n')

print('second level:')
print((4%6), (-4%6), (4%-6), (-4%-6))

print('''By default there are only two result of operations that returns a float number,
      though, the operands are integers''')
print(6/2)
print(2e6)
