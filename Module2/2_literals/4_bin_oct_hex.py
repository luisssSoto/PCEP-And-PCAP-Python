'''What about octal and hexadecimal numbers'''
print("Welcome to binary, octal and hexadecimal numbers")
print("Octal number: ", 0o123)
print("hexadecimal number: ", 0x123)
print("Binary number:", 0b1010, "\n")

'''Casting'''
decimalNumber=20
print(decimalNumber)

#decimal - binary
binNum=bin(decimalNumber)
print(binNum)

#decimal - octal
octNum=oct(decimalNumber)
print(octNum)

#decimal - hexadecimal
hexNum=hex(decimalNumber)
print(hexNum)

