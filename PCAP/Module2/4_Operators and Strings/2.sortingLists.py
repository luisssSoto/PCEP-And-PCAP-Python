"""Sorting Lists"""
'''Sorting lists which has an strings elements has the same criteria
speaking about the point of code, there are two ways to do it'''

'''1. sorted() function'''
jungle = ['tiger', 'bear', 'mono']
print(jungle)
sortJungle = sorted(jungle)
print(sortJungle)

'''2. .sort() method'''
jungle.sort()
print(jungle)