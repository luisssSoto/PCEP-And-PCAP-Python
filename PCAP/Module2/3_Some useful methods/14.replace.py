'''.replace() method'''
print('python is a snake'.replace('snake', 'serpiente'))
print('coco drile'.replace(' ', ''))
print('cockrach'.replace('cockrach', ''))
print('squirrel'.replace('', '!'))

# Note: this method works with all the occurrences
print('my candy is really sweet'.replace('y', 'i'))

'''with three arguments'''
print('my candy is really sweet'.replace('y', 'i', 2))

